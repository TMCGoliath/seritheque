    public function findSeriesOnlyReturning(): array
    {
        return $this->createQueryBuilder('s')
            ->andWhere('s.status = :status')
            ->setParameter(':status', 'returning')
            ->andWhere('s.vote > :vote ')
            ->setParameter(':vote', 8)
            ->addOrderBy('s.firstAirDate', 'DESC')
            ->setFirstResult(0)
            ->setMaxResults(20)
            ->getQuery()
            ->getResult();
    }


